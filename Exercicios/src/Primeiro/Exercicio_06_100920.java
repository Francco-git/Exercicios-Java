package Primeiro;

import java.util.Scanner;

public class Exercicio_06_100920 {

	public static void main(String[] args) {
		float n, media=0, cont=0;
		
		Scanner ler = new Scanner (System.in);
		
		do {
			System.out.println("Digite um número múltiplo de 3 para entrar na média ou digite zero para sair encerrar.");
			n = ler.nextFloat();
			if(n%3==0) {
			media = media + n;
			cont++;
			}
		}while(n!=0);
		System.out.println("A média dos múltiplos de 3 é: "+(media/(cont-1)));

	}

}